console.log(Math);
console.log(Math.PI);

//abs (valor absoluto)
console.log(Math.abs(-7.89));

//ceil (redondear numero, hacia arriba)
console.log(Math.ceil(8.5));

//floor(redondear numero, hacia abajo)
console.log(Math.floor(8.5));

//round (redondea al mayor inmediato)
console.log(Math.round(8.5));

//sqrt (raiz cuadrada)
console.log(Math.sqrt(81));

//pow (elevacion)
console.log(Math.pow(2, 3));

//random (valor aleatorio)
console.log(Math.random());
console.log(Math.random() * 1000);
console.log(Math.round(Math.random() * 1000));